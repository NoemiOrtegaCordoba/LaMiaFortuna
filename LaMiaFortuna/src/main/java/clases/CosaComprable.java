package clases;

/**
 *
 * @author noemi
 */
public class CosaComprable {

    private double precio;

    public CosaComprable(double precio) {
        this.precio = precio;
    }

    public double getPrecio() {
        return precio;
    }

    public void setPrecio(double precio) {
        this.precio = precio;
    }

    @Override
    public String toString() {
        return "CosaComprable{" + "precio=" + precio + '}';
    }

}
